namespace CustomMethodLib
{
    public class CalculateLib
    {
        public CalculateLib()
        {
        }

        public int AddtionProcess(int augend , int addend)
        {
            return augend + addend;
        }

        public int MinusProcess(int minuend , int subtrahend)
        {
            return minuend - subtrahend;
        }

        public int MultiplicationProcess(int multiplicand , int multiplier)
        {
            return multiplicand * multiplier;
        }

        public int DivisionProcess(int dividend , int divisor)
        {
            return dividend / divisor;
        }
    }
}
